    private void jButton_busca_clienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_busca_clienteActionPerformed
        String clienteBuscar = txt_cliente_buscar.getText().trim(); //Obtiene la DNI ingresada por el usuario
        Connection cn = Conexion.conectar(); //Conecta con la base de datos
        String sql = "select nombre, apellido from clientes where dni = '" + clienteBuscar + "'"; //Consulta SQL por cédula
        Statement st;
        try {
            st = cn.createStatement(); //Crea objeto para ejecutar la consulta
            ResultSet rs = st.executeQuery(sql); //Ejecuta la consulta

            if (rs.next()) { //Si encuentra al cliente
                jComboBox_cliente.setSelectedItem(rs.getString("nombre") + " " + rs.getString("apellido")); //Carga nombre completo en el combo
            } else { //Si no existe el cliente
                jComboBox_cliente.setSelectedItem("Seleccione cliente:"); //Vuelve al valor por defecto
                JOptionPane.showMessageDialog(null, "¡Documento de cliente incorrecta o no encontrada!"); //Mensaje de advertencia
            }
            txt_cliente_buscar.setText(""); //Limpia campo de búsqueda
            cn.close(); //Cierra conexión
        } catch (SQLException e) {
            System.out.println("¡Error al buscar cliente!, " + e); //Muestra error
        }
    }//GEN-LAST:event_jButton_busca_clienteActionPerformed

    private void jButton_añadir_productoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_añadir_productoActionPerformed

        // ===== VALIDAR CLIENTE =====
        if (jComboBox_cliente.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un cliente");
            return;
        }

        // ===== VALIDAR PRODUCTO =====
        if (jComboBox_producto.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un producto");
            return;
        }

        // ===== VALIDAR CANTIDAD VACÍA =====
        if (txt_cantidad.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese la cantidad");
            return;
        }

        // ===== VALIDAR QUE SEA NUMÉRICA =====
        if (!validar(txt_cantidad.getText())) {
            JOptionPane.showMessageDialog(null, "La cantidad debe ser numérica");
            return;
        }

        int cantidad = Integer.parseInt(txt_cantidad.getText());

        // ===== VALIDAR MAYOR A CERO =====
        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(null, "La cantidad debe ser mayor a cero");
            return;
        }

        // ===== OBTENER DATOS DEL PRODUCTO DESDE BD =====
        DatosDelProducto();   // ← USA TU MÉTODO

        // ===== VALIDAR STOCK =====
        if (cantidad > cantidadProductoBBDD) {
            JOptionPane.showMessageDialog(null, "Stock insuficiente");
            return;
        }

        // ===== CALCULAR TOTAL =====
        double totalProducto = precioUnitario * cantidad;
        totalProducto = Math.round(totalProducto * 100) / 100.0;

        // ===== CREAR OBJETO VENTAS =====
        producto = new Ventas(
                auxIdDetalle,
                1,
                idProducto,
                nombre,
                cantidad,
                precioUnitario,
                totalProducto
        );

        // ===== AGREGAR A LA LISTA =====
        listaProductos.add(producto);
        auxIdDetalle++;

        // ===== ACTUALIZAR TABLA =====
        listaTablaProductos();

        // ===== RECALCULAR TOTAL GENERAL =====
        CalcularTotalPagar();

        // ===== CONTROLAR MEDIO DE PAGO =====
        controlarMedioPago();

        // ===== LIMPIAR CAMPOS =====
        txt_cantidad.setText("");
        jComboBox_producto.setSelectedIndex(0);

        JOptionPane.showMessageDialog(null, "Producto agregado correctamente");


    }//GEN-LAST:event_jButton_añadir_productoActionPerformed

    private void jButton_calcular_cambioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_calcular_cambioActionPerformed
        // 1️⃣ Validar que haya seleccionado medio de pago
        String formaDePago = String.valueOf(jComboBox_medioPago.getSelectedItem());

        if (formaDePago.equals("Seleccione medio de pago")) {
            JOptionPane.showMessageDialog(null, "Seleccione el medio de pago");
            return;
        }

        String medioPago = jComboBox_medioPago.getSelectedItem().toString();

        // ️⃣ Si es transferencia
        if (medioPago.equals("TRANSFERENCIA")) {

            JOptionPane.showMessageDialog(null,
                    "Pago por transferencia registrado.\nNo se calcula vuelto.");

            txt_cambio.setText("0");
            return;
        }

        // 3️⃣ Si es efectivo
        if (medioPago.equals("EFECTIVO")) {

            if (txt_total_pagar.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "No hay total a pagar");
                return;
            }

            if (txt_pago.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Ingrese el monto recibido");
                return;
            }

            try {
                double total = Double.parseDouble(txt_total_pagar.getText());
                double pago = Double.parseDouble(txt_pago.getText());

                if (pago < total) {
                    JOptionPane.showMessageDialog(null,
                            "El monto ingresado es menor al total a pagar");
                    return;
                }

                double cambio = pago - total;
                txt_cambio.setText(String.valueOf(cambio));

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Ingrese valores numéricos válidos");
            }
        }
    }//GEN-LAST:event_jButton_calcular_cambioActionPerformed

