    private void controlarMedioPago() {

        if (jComboBox_medioPago.getSelectedItem() == null) {
            return;
        }

        String medioPago = jComboBox_medioPago.getSelectedItem().toString();

        if (medioPago.equals("EFECTIVO")) {

            txt_pago.setEnabled(true);
            txt_cambio.setEnabled(false);
            jButton_calcular_cambio.setEnabled(true);
            txt_cambio.setText("");

        } else if (medioPago.equals("TRANSFERENCIA")) {

            txt_pago.setText(txt_total_pagar.getText());
            txt_pago.setEnabled(false);
            txt_cambio.setText("0");
            txt_cambio.setEnabled(false);
            jButton_calcular_cambio.setEnabled(false);

        } else {

            txt_pago.setEnabled(false);
            txt_cambio.setEnabled(false);
            jButton_calcular_cambio.setEnabled(false);
            txt_pago.setText("");
            txt_cambio.setText("");
        }
    }

