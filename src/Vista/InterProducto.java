    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Producto producto = new Producto();
        Ctrl_Producto controlProducto = new Ctrl_Producto();
        String categoria = jComboBox_categoria.getSelectedItem().toString().trim();

        // VALIDAR CAMPOS
        if (txtNombre.getText().equals("")
                || txtStock.getText().equals("")
                || txtPrecio.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Complete todos los campos");
            txtNombre.setBackground(Color.red);
            txtStock.setBackground(Color.red);
            txtPrecio.setBackground(Color.red);
            txtCodigo.setBackground(Color.red);
            return;
        }

        // VALIDAR SI EL PRODUCTO YA EXISTE
        if (controlProducto.existeProducto(txtNombre.getText().trim())) {
            JOptionPane.showMessageDialog(null, "El producto ya existe en la Base de Datos");
            return;
        }

        // VALIDAR CATEGOR√çA
        if (categoria.equalsIgnoreCase("Seleccione categoria:")) {
            JOptionPane.showMessageDialog(null, "Seleccione una categor√≠a");
            return;
        }

        try {
            // SETEAR DATOS AL OBJETO
            producto.setNombre(txtNombre.getText().trim());
            producto.setStock(Integer.parseInt(txtStock.getText().trim()));

            // VALIDAR PRECIO (coma o punto)
            String precioTXT = txtPrecio.getText().trim().replace(",", ".");
            double precio = Double.parseDouble(precioTXT);
            producto.setPrecio(precio);

            producto.setDescripcion(txtDescripcion.getText().trim());
            producto.setCodigo(txtCodigo.getText().trim());

            // OBTENER ID DE CATEGOR√çA
            this.IdCategoria();
            producto.setIdCategoria(obtenerIdCategoriaCombo);

            producto.setEstado(1);

            // GUARDAR
            if (controlProducto.guardar(producto)) {
                JOptionPane.showMessageDialog(null, "Registro Guardado");

                txtNombre.setBackground(Color.green);
                txtStock.setBackground(Color.green);
                txtPrecio.setBackground(Color.green);
                txtDescripcion.setBackground(Color.green);
                txtCodigo.setBackground(Color.green);

                this.CargarComboCategorias();
                this.Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error en el formato del precio o stock");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error inesperado: " + e.getMessage());
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = tablaProductos.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un producto para actualizar");
            return;
        }
        if (txtPrecio.getText().trim().isEmpty() || txtStock.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos");
            return;
        }
        try {
            // OBTENER ID DESDE LA TABLA
            int idProducto = Integer.parseInt(
                    tablaProductos.getValueAt(fila, 0).toString()
            );
            String nombre = txtNombre.getText().trim();
            double precio = Double.parseDouble(
                    txtPrecio.getText().trim().replace(",", ".")
            );
            int stock = Integer.parseInt(txtStock.getText().trim());
            String descripcion = txtDescripcion.getText().trim();
            String codigo = txtCodigo.getText().trim();
            // ID CATEGOR√çA
            this.IdCategoria();

            Producto p = new Producto();
            p.setIdProducto(idProducto);   // üî¥ CLAVE
            p.setNombre(nombre);
            p.setStock(stock);
            p.setPrecio(precio);
            p.setDescripcion(descripcion);
            p.setCodigo(codigo);
            p.setIdCategoria(obtenerIdCategoriaCombo);
            p.setEstado(1);

            dao.actualizar(p);
            listar();
            limpiar();

            JOptionPane.showMessageDialog(this, "Producto actualizado Correctamente");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error en el formato de precio o stock");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

